#include "libft.h"

char	*ft_strnstr(const char *str, const char *to_find, size_t len)
{
	size_t	i;
	int		j;

	if (*to_find == '\0')
		return ((char *)str);
	if (len == 0)
		return (0);
	i = 0;
	while (str[i])
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[j + 1] == '\0')
				return ((char *)str + i);
			j++;
		}
		i++;
		if (i == len - 1)
			return (0);
	}
	return (0);
}
